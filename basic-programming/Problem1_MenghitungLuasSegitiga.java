
public class Problem1_MenghitungLuasSegitiga {
    public static void main(String[] args) {
        // input alas, tinggi
        float alas = 20;
        float tinggi = 25;

        // output hasil luas segitiga dengan tipe integer / bilangan bulat
        int value = (int) (alas * tinggi / 2);

        // print variable value
        System.out.println(value);
    }
}
